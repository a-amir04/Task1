# 5. Возраст
age = int(input("Введите возраст: "))

if 10 < age < 15:
    print(f'{age} лет')
elif 0 < age % 10 < 5:
    if age % 10 == 1:
        print(f'{age} год')
    else:
        print(f'{age} года')
else:
    print(f'{age} лет')