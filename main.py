# 3. Шифр Цезаря
word = [letter for letter in input("Введите слово: ")]
secret_key = int(input("Введите секретный ключ: "))
encrypted_word = []

for i in word:
    encrypted_word.append(chr(ord(i) - secret_key)) # Используя методы для таблицы ASCII, шифруем слово, сдвигая позицию буквы налево на значение переменной secret_key

for l in encrypted_word:
    print(l, end='')
print('')

